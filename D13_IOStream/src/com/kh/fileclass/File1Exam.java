package com.kh.fileclass;

import java.io.File;
import java.io.IOException;

public class File1Exam {

	public static void main(String[] args) {
		/*
		 * 1. ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
		 * 2. ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
		 * 3. ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ศฎ๏ฟฝ๏ฟฝ
		 * 4. ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ศฎ๏ฟฝ๏ฟฝ
		 */
		makeFile();
		makeDirectory();
		makeDirectories();
		makeFileInDirectory();
	}

	// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	static void makeFile() {
		File file = new File("khfile.txt");
		try {
			file.createNewFile();
		} catch (IOException e) {
			e.printStackTrace();
		}
	}

	// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	static void makeDirectory() {
		File file = new File("C:\\kh");
		System.out.println("kh๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝยดยฐ๏ฟฝ?: " + !file.isFile()); // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ศฎ๏ฟฝ๏ฟฝ
		file.mkdir();
	}

	// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	static void makeDirectories() {
		File file = new File("C:\\kh1\\kh2\\kh3");
		System.out.println("kh3๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝยดยฐ๏ฟฝ?: " + !file.isFile()); // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ศฎ๏ฟฝ๏ฟฝ
		file.mkdirs();
	}

	// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝศฟ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	static void makeFileInDirectory() {
		File file = new File("C:\\kh\\khtext.txt");
		try {
			file.createNewFile();
			System.out.println("๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯดยฐ๏ฟฝ?: " + file.exists()); // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ศฎ๏ฟฝ๏ฟฝ
			System.out.println("khtext.txt๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝยดยฐ๏ฟฝ?: " + file.isFile()); // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ศฎ๏ฟฝ๏ฟฝ

			if (file.delete()) // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
				System.out.println("๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝวพ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯด๏ฟฝ.");
			System.out.println("๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯดยฐ๏ฟฝ?: " + file.exists());
		} catch (IOException e) {
			e.printStackTrace();
		}
	}
}
