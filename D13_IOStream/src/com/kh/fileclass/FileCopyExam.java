package com.kh.fileclass;

import java.io.FileInputStream;
import java.io.FileNotFoundException;
import java.io.FileOutputStream;
import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStream;

public class FileCopyExam {

	public static void main(String[] args) {
		// ๏ฟฝฬน๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ

		InputStream is = null;
		OutputStream os = null;

		try {
			// ๏ฟฝฬน๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝฯต๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝฬณสธ๏ฟฝ ๏ฟฝฺต๏ฟฝ(๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ)๏ฟฝ๏ฟฝ ๏ฟฝวพ๏ฟฝ ๏ฟฝึฑ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
			// ๏ฟฝุด๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฦฎ ๏ฟฝ่ฟญ๏ฟฝ๏ฟฝ ๏ฟฝะฐ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝวธ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
			is = new FileInputStream("C:\\tmp\\sk8er.png");
			os = new FileOutputStream("C:\\Users\\82107\\Desktop\\copied.png");
			
			int readByteNo;
			byte[] readBytes = new byte[100];
			
			while ((readByteNo = is.read(readBytes)) != -1) {
				os.write(readBytes, 0, readByteNo);
			}
			os.flush();
			System.out.println("๏ฟฝ๏ฟฝ๏ฟฝ็ฐก ๏ฟฝฯท๏ฟฝวพ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯด๏ฟฝ.");
			
		} catch (FileNotFoundException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		} finally {
			try {
				is.close();
				os.close();
			} catch (IOException e) {
				e.printStackTrace();
			}
		}
	}
}
