package com.kh.operator;

public class ComparisonExam {
	public static void main(String[] args) {
		System.out.println("비교 연산자 예제");
		
		int a = 50;
		int b = 30;
		
		System.out.println("a = " + a + ", b = " + b);
		System.out.println("a!=b	" + (a!=b));
		System.out.println("a<b	" + (a<b));
		System.out.println("a==b	" + (a==b));
		System.out.println("a>b	" + (a>b));
		System.out.println("a!=b	" + (a!=b));
	}
}
